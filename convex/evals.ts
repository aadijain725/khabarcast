"use node";

// phase 4 (MAAS): eval pipeline — runs the composer agent over a fixed set
// of golden source URLs with the default hosts, then claude-as-judge scores
// each output on a 5-criterion rubric. Manual invocation:
//
//   npx convex run 'evals:runScorecardInternal' '{"userTokenId":"eval-1"}'
//
// Output is a JSON scorecard. Pipe to a file for later comparison:
//
//   npx convex run 'evals:runScorecardInternal' '{"userTokenId":"eval-1"}' \
//     > evals/scorecard-$(date +%Y%m%d-%H%M).json
//
// To compare two prompt versions, run twice (with the prompt text version
// bumped between runs) and diff the resulting scorecard files.

import Anthropic from "@anthropic-ai/sdk";
import { v } from "convex/values";
import { internalAction } from "./_generated/server";
import { internal } from "./_generated/api";
import { Id } from "./_generated/dataModel";
import { doFetch } from "./pipeline/fetchSource";
import { doCompose } from "./agents/composer";

const JUDGE_MODEL = "claude-sonnet-4-6";
const JUDGE_PROMPT_VERSION = "judge-v1-2026-04-25";

// Golden set: 3 substack articles known to produce reasonable output.
// Add more by appending to this array — schema is { feedUrl, expectedThemes }.
const GOLDEN: { feedUrl: string; expectedThemes: string[] }[] = [
  {
    feedUrl: "https://www.noahpinion.blog/feed",
    expectedThemes: ["economics", "policy", "national", "industrial"],
  },
  {
    feedUrl: "https://astralcodexten.substack.com/feed",
    expectedThemes: ["rationality", "science", "evidence", "argument"],
  },
  {
    feedUrl: "https://www.slowboring.com/feed",
    expectedThemes: ["politics", "policy", "tradeoffs", "constraints"],
  },
];

type Score = {
  factualFidelity: number; // 1-5: are claims grounded in source?
  personaConsistency: number; // 1-5: does each speaker stay in lens?
  debateDynamic: number; // 1-5: does anchor actually push back?
  lengthAdherence: number; // 1-5: 500-800 words target?
  hookQuality: number; // 1-5: opening grab?
  total: number; // sum, max 25
  notes: string;
};

const JUDGE_SYSTEM = `You are evaluating a podcast script generated by an AI agent system. The script is a two-host dialogue with two speakers: KALAM (a Kalam-inspired wise systems-thinker) and ANCHOR (a sharp skeptical news anchor). Your job is to score the script on 5 criteria, output strict JSON only.

# Criteria (each 1-5)

1. factualFidelity — every claim is grounded in the source article. No invented facts. 5 = nothing invented; 1 = mostly hallucinated.
2. personaConsistency — KALAM stays calm/synthetic, ANCHOR stays sharp/skeptical. 5 = each turn instantly identifiable; 1 = they sound the same.
3. debateDynamic — ANCHOR genuinely pushes back at least once per topic with a real counter-argument (not "but consider this" filler). 5 = friction is real; 1 = soft summary.
4. lengthAdherence — total dialogue 500-800 words, turns short (1-3 sentences). 5 = on target; 1 = far off.
5. hookQuality — opening turn earns attention without being clickbaity. 5 = sharp specific lede; 1 = generic.

# Output (strict JSON, no fences)

{
  "factualFidelity": <int 1-5>,
  "personaConsistency": <int 1-5>,
  "debateDynamic": <int 1-5>,
  "lengthAdherence": <int 1-5>,
  "hookQuality": <int 1-5>,
  "notes": "1-2 sentences on what stood out or what's weak"
}

Score conservatively. 5 means actually impressive, not just "fine".`;

function buildJudgeUser(sourceTitle: string, sourceText: string, dialogueJson: string): string {
  return `# Source article

Title: ${sourceTitle}

${sourceText}

# Generated dialogue (JSON)

${dialogueJson}`;
}

function stripFences(s: string): string {
  const t = s.trim();
  const m = t.match(/^```(?:json)?\s*([\s\S]*?)\s*```$/);
  return m ? m[1].trim() : t;
}

function validateScore(raw: unknown): Score {
  if (!raw || typeof raw !== "object") throw new Error("not an object");
  const r = raw as Record<string, unknown>;
  const fields = [
    "factualFidelity",
    "personaConsistency",
    "debateDynamic",
    "lengthAdherence",
    "hookQuality",
  ] as const;
  const out: Partial<Score> = {};
  for (const f of fields) {
    const v = r[f];
    if (typeof v !== "number" || !Number.isInteger(v) || v < 1 || v > 5)
      throw new Error(`${f} must be integer 1-5`);
    out[f] = v;
  }
  if (typeof r.notes !== "string") throw new Error("notes must be string");
  out.notes = r.notes;
  out.total =
    out.factualFidelity! +
    out.personaConsistency! +
    out.debateDynamic! +
    out.lengthAdherence! +
    out.hookQuality!;
  return out as Score;
}

export const runScorecardInternal = internalAction({
  args: {
    userTokenId: v.string(),
    kalamHostId: v.optional(v.id("hosts")),
    anchorHostId: v.optional(v.id("hosts")),
    // Optional: limit to first N golden items. Default = all. Useful when
    // running interactively to dodge convex action duration spikes.
    limit: v.optional(v.number()),
  },
  handler: async (ctx, args) => {
    const apiKey = process.env.ANTHROPIC_API_KEY;
    if (!apiKey) throw new Error("ANTHROPIC_API_KEY not set");

    // Resolve hosts: use provided or first global of each slot.
    let kalamHostId = args.kalamHostId;
    let anchorHostId = args.anchorHostId;
    if (!kalamHostId || !anchorHostId) {
      const globals = await ctx.runQuery(internal.hosts.listGlobalsInternal, {});
      const k = globals.find((h) => h.slot === "KALAM");
      const a = globals.find((h) => h.slot === "ANCHOR");
      if (!k || !a) throw new Error("no global hosts seeded — run hosts:seedDefaultsInternal");
      kalamHostId = k._id;
      anchorHostId = a._id;
    }

    const startedAt = Date.now();
    const results: Array<{
      feedUrl: string;
      sourceTitle: string;
      episodeId?: Id<"episodes">;
      score?: Score;
      error?: string;
      latencyMs: number;
    }> = [];

    const client = new Anthropic({ apiKey });

    const items = args.limit ? GOLDEN.slice(0, args.limit) : GOLDEN;
    for (const golden of items) {
      const itemStart = Date.now();
      try {
        // Fetch one source from this feed (helper composition, same-runtime).
        const fetchRes = await doFetch(ctx, {
          feedUrl: golden.feedUrl,
          userTokenId: args.userTokenId,
        });
        // Run composer (with default hosts).
        const compose = await doCompose(ctx, {
          sourceId: fetchRes.sourceId,
          userTokenId: args.userTokenId,
          hostMapping: { KALAM: kalamHostId, ANCHOR: anchorHostId },
        });
        // Read back the episode.
        const episode = await ctx.runQuery(internal.episodes.getInternal, {
          episodeId: compose.episodeId,
        });
        if (!episode) throw new Error("episode not found after compose");

        // Reload the source to feed to the judge.
        const source = await ctx.runQuery(internal.sources.getOwnedInternal, {
          sourceId: fetchRes.sourceId,
          userTokenId: args.userTokenId,
        });

        const dialogueJson = JSON.stringify(episode.dialogue, null, 2);
        const resp = await client.messages.create({
          model: JUDGE_MODEL,
          max_tokens: 1024,
          system: JUDGE_SYSTEM,
          messages: [
            {
              role: "user",
              content: buildJudgeUser(source.title, source.rawText, dialogueJson),
            },
          ],
        });
        const text = resp.content.find((b) => b.type === "text");
        if (!text || text.type !== "text") throw new Error("no text in judge response");
        const score = validateScore(JSON.parse(stripFences(text.text)));

        results.push({
          feedUrl: golden.feedUrl,
          sourceTitle: fetchRes.title,
          episodeId: compose.episodeId,
          score,
          latencyMs: Date.now() - itemStart,
        });
      } catch (err) {
        results.push({
          feedUrl: golden.feedUrl,
          sourceTitle: "(fetch failed)",
          error: err instanceof Error ? err.message : String(err),
          latencyMs: Date.now() - itemStart,
        });
      }
    }

    // Aggregate.
    const scored = results.filter((r) => r.score);
    const avg = (key: keyof Score): number => {
      if (scored.length === 0) return 0;
      const total = scored.reduce(
        (s, r) => s + (r.score![key] as number),
        0,
      );
      return Math.round((total / scored.length) * 100) / 100;
    };

    return {
      judgeModel: JUDGE_MODEL,
      judgePromptVersion: JUDGE_PROMPT_VERSION,
      runAt: new Date(startedAt).toISOString(),
      totalLatencyMs: Date.now() - startedAt,
      itemCount: results.length,
      scoredCount: scored.length,
      averages: {
        factualFidelity: avg("factualFidelity"),
        personaConsistency: avg("personaConsistency"),
        debateDynamic: avg("debateDynamic"),
        lengthAdherence: avg("lengthAdherence"),
        hookQuality: avg("hookQuality"),
        total: avg("total"),
      },
      items: results,
    };
  },
});
